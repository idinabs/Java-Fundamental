package com;

public class tipeData {
    public static void main(String[] args) {
        // tipe data java
        // int, byte, short, long, double, float, char, boolean

        //int (satuan)
        int a = 10;
        System.out.println("=====INTEGER=====");
        System.out.println("nilai a = " + a);
        System.out.println("nilai max integer = " + Integer.MAX_VALUE);
        System.out.println("nilai min integer = " + Integer.MIN_VALUE);
        System.out.println("nilai byte integer = " + Integer.BYTES);
        System.out.println("nilai bit integer = " + Integer.SIZE);

        //byte (satuan)
        byte b = 10;
        System.out.println("=====BYTE=====");
        System.out.println("nilai b = " + b);
        System.out.println("nilai max byte = " + Byte.MAX_VALUE);
        System.out.println("nilai min byte = " + Byte.MIN_VALUE);
        System.out.println("nilai byte dari byte = " + Byte.BYTES);
        System.out.println("nilai bit dari byte = " + Byte.SIZE);

        //short (satuan)
        short c = 10;
        System.out.println("=====SHORT=====");
        System.out.println("nilai c = " + c);
        System.out.println("nilai max dari short = " + Short.MAX_VALUE);
        System.out.println("nilai min dari short = " + Short.MIN_VALUE);
        System.out.println("nilai byte dari short = " + Short.BYTES);
        System.out.println("nilai bit dari short = " + Short.SIZE);

        //long (satuan)
        long d = 10;
        System.out.println("=====LONG=====");
        System.out.println("nilai d = " + d);
        System.out.println("nilai max dari long = " + Long.MAX_VALUE);
        System.out.println("nilai min dari long = " + Long.MIN_VALUE);
        System.out.println("nilai byte dari long = " + Long.BYTES);
        System.out.println("nilai bit dari long = " + Long.SIZE);

        //double (bilangan real)
        double e = 10;
        System.out.println("=====LONG=====");
        System.out.println("nilai e = " + e);
        System.out.println("nilai max dari double = " + Double.MAX_VALUE);
        System.out.println("nilai min dari double = " + Double.MIN_VALUE);
        System.out.println("nilai byte dari double = " + Double.BYTES);
        System.out.println("nilai bit dari double = " + Double.SIZE);

        //float (bilangan real)
        float f = 10;
        System.out.println("=====DOUBLE=====");
        System.out.println("nilai f = " + f);
        System.out.println("nilai max dari float = " + Float.MAX_VALUE);
        System.out.println("nilai min dari float = " + Float.MIN_VALUE);
        System.out.println("nilai byte dari float = " + Float.BYTES);
        System.out.println("nilai bit dari float = " + Float.SIZE);

        //char
        char g = 'H';
        System.out.println("=====CHAR=====");
        System.out.println("nilai g = " + g);
        System.out.println("nilai max dari char = " + Character.MAX_VALUE);
        System.out.println("nilai min dari char = " + Character.MIN_VALUE);
        System.out.println("nilai byte dari char = " + Character.BYTES);
        System.out.println("nilai bit dari char = " + Character.SIZE);

        //bolean
        boolean h = true;
        System.out.println("=====BOOLEAN=====");
        System.out.println("nilai g = " + h);
        System.out.println("nilai byte dari boolean = " + Boolean.TRUE);
        System.out.println("nilai bit dari boolean = " + Boolean.FALSE);
    }
}
